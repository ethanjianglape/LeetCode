// https://leetcode.com/problems/palindrome-number/description/

namespace LeetCode.Solutions;

internal class PalindromeNumber
{
    public static bool IsPalindrome(int x)
    {
        if (x < 0)
        {
            return false;
        }

        if (x < 10)
        {
            return true;
        }

        int num1 = x;
        int num2 = 0;

        while (x > 0)
        {
            int digit = x % 10;

            num2 *= 10;
            num2 += digit;

            x /= 10;
        }

        return num1 == num2;
    }
}
